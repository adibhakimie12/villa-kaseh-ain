export function translateSiteContent(content: SiteContent, language: Language): SiteContent {
  return {
    ...content,
    siteConfig: {
      ...content.siteConfig,
      tagline: translate(language, 'content.tagline'),
      whatsappMessage: translate(language, 'content.whatsappMessage'),
    },
    villaFeatures: content.villaFeatures.map((feature, index) => {
      const keys = featureTranslationKeys[index];
      return keys
        ? {
            ...feature,
            title: translate(language, keys.title),
            description: translate(language, keys.description),
          }
        : feature;
    }),
    roomTypes: content.roomTypes.map((room) => {
      const keys = roomTranslationKeys[room.id];
      return keys
        ? {
            ...room,
            label: translate(language, keys.label),
            period: translate(language, keys.period),
            note: translate(language, keys.note),
          }
        : room;
    }),
    stayRules: {
      ...content.stayRules,
      responseWindow: translate(language, 'content.responseWindow'),
    },
    stayInformation: stayInfoTranslations[language],
    manualPayment: {
      ...content.manualPayment,
      instructions: translate(language, 'content.manualPayment.instructions'),
    },
  };
}
