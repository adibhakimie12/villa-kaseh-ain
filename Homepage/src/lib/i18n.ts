export type TranslationKey = keyof typeof translations.en;

export function interpolate(template: string, values?: TranslationValues) {
  if (!values) return template;
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template,
  );
}

export function translate(language: Language, key: TranslationKey, values?: TranslationValues) {
  return interpolate(translations[language][key] ?? translations.en[key] ?? key, values);
}

export function readStoredLanguage(): Language {
  if (typeof window === 'undefined') return DEFAULT_LANGUAGE;
  const value = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return value === 'ms' || value === 'en' ? value : DEFAULT_LANGUAGE;
}

export function getLanguageLocale(language: Language) {
  return language === 'ms' ? 'ms-MY' : 'en-MY';
}

export function formatLongDateByLanguage(value: string, language: Language) {
  return new Intl.DateTimeFormat(getLanguageLocale(language), {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  }).format(new Date(`${value}T00:00:00`));
}

export function getRoomRatePriceCaptionByLanguage(rate: { pricingType?: string; packageNights?: number | null }, language: Language) {
  if (rate.pricingType === 'package') {
    return language === 'ms' ? `Pakej ${rate.packageNights} malam` : `Package ${rate.packageNights} nights`;
  }
  return language === 'ms' ? 'Semalam' : 'Per night';
}

